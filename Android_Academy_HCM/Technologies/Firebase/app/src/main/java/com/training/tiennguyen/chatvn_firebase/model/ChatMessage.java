package com.training.tiennguyen.chatvn_firebase.model;

/**
 * ChatMessage
 *
 * Created by TienNguyen on 06/03/2016.
 */
public class ChatMessage {
    private String name;
    private String text;

    public ChatMessage() {
        // Necessary for Firebase's deserializer
    }

    public ChatMessage(String name, String text) {
        this.name = name;
        this.text = text;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getText() {
        return text;
    }

    public void setText(String text) {
        this.text = text;
    }
}
